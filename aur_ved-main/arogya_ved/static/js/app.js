// Current language
let currentLang = localStorage.getItem('lang') || 'en-IN';

// Translate text
function t(key) {
  return translations[currentLang][key] || translations['en-IN'][key] || key;
}

// Apply translations to page
function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    el.placeholder = t(key);
  });
  // Update language selector text
  document.querySelectorAll('.lang-select').forEach(el => {
    const option = Array.from(el.options).find(o => o.value === currentLang);
    if (option) el.value = currentLang;
  });
}

// Initialize language on load
document.addEventListener('DOMContentLoaded', function() {
  applyTranslations();
  // Add language change listeners
  document.querySelectorAll('.lang-select').forEach(el => {
    el.addEventListener('change', function() {
      currentLang = this.value;
      localStorage.setItem('lang', currentLang);
      applyTranslations();
    });
  });
});

// Toast notifications
function showToast(msg, type = 'info') {
  const container = document.getElementById('toasts');
  if (!container) return;
  const toast = document.createElement('div');
  toast.className = `toast ${type}`;
  toast.textContent = msg;
  container.appendChild(toast);
  setTimeout(() => {
    toast.style.opacity = '0';
    toast.style.transform = 'translateX(20px)';
    setTimeout(() => toast.remove(), 300);
  }, 3500);
}

// Theme toggle
function toggleTheme() {
  const html = document.documentElement;
  const current = html.getAttribute('data-theme');
  const next = current === 'dark' ? 'light' : 'dark';
  html.setAttribute('data-theme', next);
  localStorage.setItem('theme', next);
  const btn = document.querySelector('.theme-toggle');
  if (btn) btn.textContent = next === 'dark' ? '☀️' : '🌙';
}

// Apply saved theme
const savedTheme = localStorage.getItem('theme');
if (savedTheme) {
  document.documentElement.setAttribute('data-theme', savedTheme);
  const btn = document.querySelector('.theme-toggle');
  if (btn) btn.textContent = savedTheme === 'dark' ? '☀️' : '🌙';
}

// API helper - handles both token auth and cookie auth
async function api(url, options = {}) {
  const token = localStorage.getItem('token');
  const headers = { ...options.headers };
  if (token) {
    headers['Authorization'] = `Bearer ${token}`;
  }
  const res = await fetch(url, { ...options, headers, credentials: 'include' });
  return res;
}

// Simple fetch wrapper for public endpoints
async function fetchApi(url, options = {}) {
  const res = await fetch(url, { ...options, credentials: 'include' });
  return res;
}

// Auth guard - call on dashboard pages
function checkAuth(expectedRole) {
  const token = localStorage.getItem('token');
  const role = localStorage.getItem('role');
  if (!token) {
    // Don't redirect on dashboard load for demo purposes - allow browsing
    console.warn('No token found, allowing demo access');
    return true;
  }
  if (expectedRole && role && role !== expectedRole) {
    // Don't redirect - allow demo access
    console.warn('Role mismatch, allowing demo access');
  }
  return true;
}

// Error-aware fetch with toast
async function safeFetch(url, options = {}) {
  try {
    const res = await fetch(url, { ...options, credentials: 'include' });
    if (!res.ok) {
      console.error(`Fetch error: ${res.status} ${res.statusText} for ${url}`);
    }
    return res;
  } catch (e) {
    console.error(`Network error fetching ${url}:`, e);
    showToast('Network error. Please check your connection.', 'error');
    throw e;
  }
}

