// Send chat message
async function sendChat(role = 'citizen') {
  const input = document.getElementById('chat-input');
  const msgs = document.getElementById('chat-msgs');
  const q = input.value.trim();
  if (!q) return;
  input.value = '';
  // Add user message
  const userMsg = document.createElement('div');
  userMsg.className = 'chat-msg user';
  userMsg.textContent = q;
  msgs.appendChild(userMsg);
  msgs.scrollTop = msgs.scrollHeight;
  // Typing indicator
  const typing = document.createElement('div');
  typing.className = 'chat-msg bot';
  typing.innerHTML = '<span style="color:#94a3b8;">🤖 Thinking...</span>';
  msgs.appendChild(typing);
  msgs.scrollTop = msgs.scrollHeight;
  try {
    const res = await fetch(`/api/ai/chatbot?q=${encodeURIComponent(q)}&role=${role}`);
    const data = await res.json();
    typing.textContent = data.response;
    const conf = document.createElement('div');
    conf.style.cssText = 'font-size:10px;color:#94a3b8;margin-top:3px;text-align:right;';
    conf.textContent = `Confidence: ${Math.round(data.confidence * 100)}%`;
    typing.appendChild(conf);
  } catch (e) {
    typing.textContent = 'Sorry, AI is unavailable right now.';
  }
  msgs.scrollTop = msgs.scrollHeight;
}

// Risk badge helper
function riskBadge(level) {
  const map = { low: 'risk-low', medium: 'risk-medium', high: 'risk-high', critical: 'risk-critical', good: 'risk-good' };
  const labels = { low: '🟢 Low', medium: '🟡 Medium', high: '🟠 High', critical: '🔴 Critical', good: '✅ Good' };
  return `<span class="risk-badge ${map[level] || 'risk-low'}">${labels[level] || level}</span>`;
}

// Format date
function fmtDate(dateStr) {
  if (!dateStr) return 'N/A';
  const d = new Date(dateStr);
  return d.toLocaleDateString('en-IN', { day: '2-digit', month: 'short', year: 'numeric' });
}

